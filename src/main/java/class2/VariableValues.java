package class2;

public class VariableValues {
    public static void main(String[] args) {
        byte number=13;
        short secondNumber=7;
        int whateverNumber=5000;
        long hugeNumber=455454544L;
        float decimalNumber= 5.22547F;
        double bigDecimal= 7878787.45454545;
        char oneLetter ='C';
        boolean trueFalseBox=false;
        System.out.println(number);
        System.out.println(secondNumber);
        System.out.println(whateverNumber);
        System.out.println(hugeNumber);
        System.out.println(decimalNumber);
        System.out.println(bigDecimal);
        System.out.println(oneLetter);
        System.out.println(trueFalseBox);
    }
}
